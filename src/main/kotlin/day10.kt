import java.util.EmptyStackException
import java.util.Stack

class Day10(input: String) {
    val input = input.split("\n")

    private val opposit = mapOf(
        '(' to ')',
        '[' to ']',
        '{' to '}',
        '<' to '>',
        ')' to '(',
        ']' to '[',
        '}' to '{',
        '>' to '<'
    )
    private val openers = listOf('(', '[', '{', '<')

    fun day10Part1(): Int {
        var errors = ""
        input.map { string ->
            val stack: Stack<Char> = Stack<Char>()
            stack.push(string[0])
            string.drop(1).forEach { char ->
                try {
                    if (stack.peek() == opposit[char]) stack.pop()
                    else if (openers.contains(char)) stack.push(char)
                    else {
                        errors += char
                        return@map
                    }
                } catch (ex: EmptyStackException) {
                    return@map
                }
            }
        }

        return errors.fold(0) { acc, char ->
            acc + when (char) {
                ')' -> 3
                ']' -> 57
                '}' -> 1197
                '>' -> 25137
                else -> throw RuntimeException()
            }
        }
    }

    fun day10Part2(): Long {

        val chars = input.mapNotNull { string ->
            val stack: Stack<Char> = Stack<Char>()
            stack.push(string[0])
            string.drop(1).forEach { char ->
                try {
                    if (stack.peek() == opposit[char]) stack.pop()
                    else if (openers.contains(char)) stack.push(char)
                    else {
                        return@mapNotNull null
                    }
                } catch (ex: EmptyStackException) {
                    stack.push(char)
                }
            }
            stack.map {
                opposit[it]!!
            }.reversed().joinToString(separator = "")
        }

        return chars.map { string ->
            string.fold(0L) { acc, char ->
                acc * 5 + when (char) {
                    ')' -> 1
                    ']' -> 2
                    '}' -> 3
                    '>' -> 4
                    else -> throw RuntimeException()
                }
            }
        }.sorted().let {
            it[it.size / 2]
        }
    }
}


